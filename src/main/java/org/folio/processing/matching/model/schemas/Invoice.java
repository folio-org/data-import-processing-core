package org.folio.processing.matching.model.schemas;

public class Invoice {

  private String id;
  private String name;

  public Invoice(String id) {
    this.id = id;
  }

  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }
}
